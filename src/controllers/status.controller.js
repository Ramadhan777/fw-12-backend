const errorHandler = require('../helpers/errorHandler')
const { selectAllStatus, selectStatus, insertStatus, patchStatus, deleteStatus, countAllStatus } = require('../models/Status.model')

exports.readAllStatus = (req, res) => {

  req.query.page = parseInt(req.query.page) || 1
  req.query.limit = parseInt(req.query.limit) || 5
  req.query.search = req.query.search || ''
  const sortable = ['name', 'createdAt', 'updatedAt']
  req.query.sortBy = (sortable.includes(req.query.sortBy) && req.query.sortBy) || 'createdAt'
  req.query.sort = req.query.sort || 'ASC'

  const filter = {
    limit: req.query.limit,
    offset: (parseInt(req.query.page - 1)) * req.query.limit,
    search: req.query.search,
    sort: req.query.sort,
    sortBy: req.query.sortBy
  }

  const pageInfo = {
    page: req.query.page
  }

  countAllStatus(filter, (err, data) => {
    if(err) {
      return errorHandler(err, res)
    }

    pageInfo.totalData = parseInt(data.rows[0].totalData)
    pageInfo.totalPage = Math.ceil(pageInfo.totalData / req.query.limit)
    pageInfo.nextPage = req.query.page < pageInfo.totalPage ? req.query.page + 1: null
    pageInfo.prevPage = req.query.page > 1 ? req.query.page - 1: null


    selectAllStatus(filter, (err, result) => {
      if(err){
        return errorHandler(err, res)
      }

      return res.status(200).json({
        success: true,
        pageInfo,
        casts: result.rows
      })
    })
  })
}

exports.readStatus = (req, res) => {
  selectStatus(req.params, (err, data) => {
    if(err){
      return errorHandler(err, res)
    }

    if(data.rows.length === 0) {
      return res.status(400).json({
        success: false,
        message: "Status not found"
      })
    }

    return res.status(200).json({
      success: true,
      Status: data.rows
    })
  })
}

exports.createStatus = (req, res) => {
  insertStatus(req.body, (err, data) => {
    if(err) {
      return errorHandler(err, res)
    }

    return res.status(200).json({
      success: true,
      message: "Status created successfully",
      Status: data.rows[0]
    })
  })
}

exports.updateStatus = (req, res) => {
  patchStatus(req.body, req.params, (err, data) => {
    if(err){
      return errorHandler(err, res)
    }

    if(data.rows.length === 0){
      return res.status(400).json({
        success: false,
        message: "Status doesn't exist"
      })
    }

    return res.status(200).json({
      success: true,
      message: "Status Updated",
      Status: data.rows
    })
  })
}

exports.deleteStatus = (req, res) => {
  deleteStatus(req.params, (err, data) => {
    if(err) {
      return errorHandler(err, res)
    }

    if(data.rows.length === 0){
      return res.status(400).json({
        success: false,
        message: "Status doesn't exist"
      })
    }

    return res.status(200).json({
      success: true,
      message: "Status Deleted",
      Status: data.rows
    })
  })
}
